// Apply language function
function applyLanguage(lang) {
    const t = translations[lang] || translations['en-uk'];

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (t[key]) {
            element.textContent = t[key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            element.placeholder = t[key];
        }
    });

    // Store current language
    window.currentLanguage = lang;
    localStorage.setItem('language', lang);
}

// Initialize language on page load
function initializeLanguage() {
    const savedLanguage = localStorage.getItem('language') || 'en-uk';
    applyLanguage(savedLanguage);
}

// Apply language when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeLanguage);
} else {
    // DOM is already loaded, apply language immediately
    initializeLanguage();
}

// Also apply on window load as a fallback to catch any late-loading elements
window.addEventListener('load', function () {
    const savedLanguage = localStorage.getItem('language') || 'en-uk';
    applyLanguage(savedLanguage);
});

// Make applyLanguage available globally for manual calls
window.applyLanguage = applyLanguage;

